// Create a new anchor element
const editorLink = document.createElement('a');

// Set the href attribute (URL) of the anchor
editorLink.href = 'https://example.com';

// Set the text content (visible text) of the anchor
editorLink.textContent = 'Click me to visit Example.com';

// Append the anchor element to the body of the document
document.body.appendChild(editorLink);
